import Foundation
import UIKit

extension UIColor {
    static func customCyanColor()  -> UIColor {
        return UIColor(red: 0.310, green: 0.800, blue: 0.969, alpha: 1.0)
    }
    static func customGrayColor() -> UIColor {
        return UIColor(red: 128/255, green: 128/255, blue: 128/255, alpha: 1.0)
    }
}
